package primeiro.progeto;

public class Variaveis {

	public static void main(String[] args) {

		int numeroInteriro = 100;
		int idade = 24;
		double numeroQuebrado = 1.5;
		double altura = 1.75;
		boolean verdadeiroFalso = false;
		String nome = "Lucas";

		System.out.println("Variavel do tipo int pode receber " + numeroInteriro);
		System.out.println("Variavel do tipo double pode receber " + numeroQuebrado);
		System.out.println("Variavel do tipo boolean pode receber " + verdadeiroFalso);
		System.out.println("Meu nome è " + nome + ".");
		System.out.println("Eu tenho " + idade + " anos.");
		System.out.println("Eu tenho " + altura + " de altura.");

	}

}
